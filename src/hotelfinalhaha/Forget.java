    private void kButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_kButton2ActionPerformed
        // TODO add your handling code here:
       String username = jTextField1.getText().trim();
    String securityAnswer = jTextField3.getText().trim();

    if (con == null) {
        JOptionPane.showMessageDialog(this, "Database connection not established.");
        return;
    }

    if (username.isEmpty() || securityAnswer.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields.");
        return;
    }

    try {
        String query = "SELECT * FROM users WHERE username = ? AND securityAnswer = ?";
        PreparedStatement pat = con.prepareStatement(query);
        pat.setString(1, username);
        pat.setString(2, securityAnswer);
        ResultSet rs = pat.executeQuery();

        if (rs.next()) {
            JOptionPane.showMessageDialog(this, "Security answer is correct!");

            // ðŸ‘‡ Proceed to new JFrame (password update form)
            ResetPass resetForm = new ResetPass(username); // pass username if needed
            resetForm.setVisible(true);
            this.dispose(); // close current form if needed

        } else {
            JOptionPane.showMessageDialog(this, "Incorrect security answer.");
            jTextField3.setText("");
        }
    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(this, "An error occurred during verification.");
        Logger.getLogger(Forget.class.getName()).log(Level.SEVERE, null, ex);
    }
    }//GEN-LAST:event_kButton2ActionPerformed

